      <p dir="ltr">The GST is being introduced not only to get rid of the current patchwork of indirect taxes that are partial and suffer from infirmities, mainly exemptions and multiple rates, but also to improve tax compliances.</p>
      <p dir="ltr">Under this regime, the taxation burden will be divided equitably between manufacturing and services, through a lower tax rate by increasing the tax base and minimizing exemptions.</p>
      <p dir="ltr">A unified GST is an economically efficient solution for all organized and unorganized sector, as it simplifies the indirect tax structure to one general rate that can be paid by all companies.&nbsp; Under this, every company gets a deduction on the taxes already paid by its suppliers. That results in every buyer ensuring that his supplier has paid his part, to claim his deductions.</p>
      <p dir="ltr">It is expected to help build a transparent and corruption-free tax administration. GST will be levied only at the destination point, and not at various points (from manufacturing to retail outlets). It is also estimated that India will gain, as implementation of GST would help promote exports, raise employment and boost growth.</p>
      <p dir="ltr">In the GST system, both central and state taxes will be collected at the point of sale. Both components (the central and state GST) will be charged on the manufacturing cost.&nbsp; This will benefit individuals as prices are likely to come down. Lower prices will lead to more consumption, thereby helping boost the economy.</p>
