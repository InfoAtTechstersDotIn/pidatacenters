      <p>Since data centers are so vital for smooth functioning of digital economy and the connected world, enterprises should be very careful in choosing one for hosting their business-critical data.</p>
      <h2>Here are a few of the elements that enterprises should consider for shortlisting data centers:</h2>
      <h3>Availability:</h3>
      <p>It is critical for an enterprise to access data at any given point of time. Therefore, round the clock availability of the data hosted in a data center is a crucial factor for consideration. An ideal data center should always remain at the disposal of its customer(s). The availability is measured in terms of Tiers. Tier IV is the global highest standard a data center could achieve, that translates to 99.995% availability of the customers data. In other words, the maximum tolerance for scheduled downtime in a Tier IV data center is less than 26 minutes in 365 days. There are a few organizations that globally audit and certify whether the infrastructure of a data center is capable enough to commit to such high-availability metrics</p>
      <h3>Security:</h3>
      <p> Ensuring complete security of the customer’s data is equally important as keeping the data available. Security of the data should be considered from two aspects:</p>
      <h2>Data center (physical) security, and Cloud security</h2>
      <p>Potential customers must carefully evaluate the location of the data center to understand how safe the site would be from natural disasters like earthquakes, floods, et al. The cloud platform, if offered by the data center, should be equipped with comprehensive Security Information and Event Management (SIEM) software to protect data at all levels. SIEM is used for identifying, monitoring, recording, and analysing security events or incidents within a real time IT environment. Data centers should be continuously innovating in this scope and deploy latest tools and techniques for protect the customer’s data. Homomorphic Encryption is one of the latest innovations in data security where data can be protected even when it is getting processed, i.e. in-transit</p>
      <h3>Performance:</h3>
      <p> It is extremely important for the modern-day enterprises to ensure that their customers can access the applications quickly (or easily). The latency for accessing the data depends on the location of the data center and its proximity to the fibre cable of the ISPs in the respective countries. It is imperative to evaluate the network accelerator software that the data center would use for ensuring data retrieval in the least possible time. Again, there are lot of innovations being carried out currently in this field with increasing needs for transferring huge amounts of data across countries and continents</p>
      <h3>Modularity & Portability:</h3>
      <p> Modular data centers are portable and easily deployable. These consist of modules that are purpose-engineered. One of the most popular form factors is container facilities. These facilities are made up of prefabricated components that can be easily and quickly set-up at a site, whenever capacity expansion is required</p>
