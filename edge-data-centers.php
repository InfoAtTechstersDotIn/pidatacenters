      <p>“Smart Cities” is one of the most popular and emerging use cases of data centers, especially in India. There are more than 100 “Smart City” projects being already announced and more than 500 “Amrut Town” projects are being planned in the country over the next few years. Data centers are expected to play a pivotal part in successfully executing these plans.</p>
