      <p>Smart cities will generate huge amount of data, and that will only continue to grow exponentially. Therefore, it is extremely important for data center organizations to have hyperscale infrastructure in place for storing such ever-growing data. Additionally, these data centers require reliable power supply for uninterrupted operations of the facilities. But these call for huge investments and equally huge manpower to run such facilities.</p>
      <p>These challenges can be mitigated to a significant extent with Edge Data Centers in place. These data centers of the future are at the edge of the last-mile network, thereby allowing the wired and wireless worlds to operate in tandem.</p>
      <h2>Data Center Adoption In The Near Future</h2>
      <p>Based on the industry research, the global data center construction market accounted for US$ 43.7 Bn in 2017 and is expected to grow at a CAGR of 10.2 percent during 2018 to 2025 and will account for US$ 92.9 Bn in 2025.</p>
      <p>The data center construction market is broadly classified into types of construction, tier/standards, industry verticals and geography. North America has always remained one of the most prominent regions for data center construction. This is because there has been early adoption of data center technology across various industries/ verticals in this part of the world. Rapidly growing economies in Asia-Pacific (APAC) region will pave the path for increasing adoption of data centers and propel the data center construction market. The APAC region is expected to lead the market globally, with highest CAGR over the next five years from now.</p>
      <h2>In my views, following are the top five factors that the data centers might want to adopt in the near future:</h2>
      <p>Smaller Data Centers: According to a popular survey across 800 data center professionals for projecting the data center market in the year 2025, 67% participants believed at least 60% of global computing will be cloud-based by the year 2025. It is also likely that the enterprise data centers would shrink in size. 58% percent of the respondents foresee that the data centers of the future will be half the size of the current facilities or smaller. Another 10% respondents believe the enterprise data center of 2025 will be one-tenth the size of the current facilities. While 41% data center professionals expect a combination of air and liquid to be the primary method for data center cooling over the next five years, only 20% of them see ambient air or free cooling, becoming the primary means for thermal management and just 9% professionals foresee the rise of immersive cooling</p>
      <p>Renewable Energy in Data Centers:Experts believe a mix of sources will be used to provide electrical power to data centers. Solar will lead, followed by a nearly equal mix of nuclear, natural gas and wind</p>
      <p>Less of Colo and More of Cloud: Industry experts predict more than 67% of data center computing globally will be done in the cloud</p>
      <p>Prominent Role of DCIM:Data Center Infrastructure Management (DCIM) is the amalgamation of IT and building facilities functions within an organization. While few of the experts prioritize comprehensive visibility across all systems and layers, the others expect the data center to be self-healing and self-optimizing. That indicates that 72% of the experts believe some level of DCIM will be deployed in almost all data centers globally by 2025</p>
      <p>Innovation and Automation: More and more innovation will continue for reducing the power usage in the data centers and for expanding more avenues for service automation in order to improve efficiency of service delivery and facilitate quicker go-to market for customers</p>
